/**
 * App bootstrap: wires core managers and components in order
 */
;(function(){
  window.AIQO = window.AIQO || {};
  AIQO.Init = AIQO.Init || {};

  var STAT_TOOLTIPS = {
    "shared-hit":     "shared hit (blocks)\nThe number of data blocks (usually 8kB) that PostgreSQL found already in its own RAM cache (the shared buffers). This is very fast.",
    "shared-read":    "shared read (blocks)\nThe number of data blocks that PostgreSQL had to read from the disk (or the OS cache) because they were not in the shared buffers. This is slow and indicates I/O cost.",
    "shared-dirtied": "dirtied (blocks)\nThe number of blocks that were modified in RAM by this query (e.g., by an INSERT or UPDATE). These blocks will need to be written to disk later. This represents the total data writing cost.",
    "shared-written": "written (blocks)\nThe number of 'dirtied' blocks that this query had to write to disk during its execution. This usually happens if the query modifies so much data that it fills up the available shared buffers.",
    "temp-read":      "temp read (blocks)\nThe number of temporary data blocks that PostgreSQL had to read from disk because the query required more working memory than was available.",
    "temp-written":   "temp written (blocks)\nThe number of temporary data blocks that were written to temporary files on disk. This is also related to operations like sorting or hash joins that require more memory than available.",
    "wal-records":    "WAL records (count)\nThe total number of 'change records' generated by this query and written to the Write-Ahead Log (WAL).",
    "wal-fpi":        "WAL fpi (count)\nThe number of 'Full Page Images' (FPI) written to the WAL. This is a special, larger type of WAL record that is generated the first time a block is modified after a checkpoint.",
    "wal-bytes":      "WAL bytes (bytes)\nThe total size (in bytes) of all WAL records generated by this query. This is a direct measure of the I/O cost for transaction logging and replication."
  };

  function initializeBootstrapTooltips() {
    document.querySelectorAll('[data-bs-toggle="tooltip"]').forEach((element) => {
      bootstrap.Tooltip.getOrCreateInstance(element, {
        container: 'body',
        trigger: 'hover focus',
      });
    });
  }

  function initializeStatTooltips() {
    document.querySelectorAll('[data-stat-tip]').forEach(function(el) {
      var text = STAT_TOOLTIPS[el.dataset.statTip];
      if (text) {
        bootstrap.Tooltip.getOrCreateInstance(el, {
          container: 'body',
          trigger: 'hover focus',
          title: text
        });
      }
    });
  }

  AIQO.Init.run = function(){
    // Core managers
    window.reportChartManager = new AIQO.Core.ReportChartManager(reportData);
    window.reportNavigator = new AIQO.Core.ReportNavigator(reportData);

    // Components
    if (AIQO.Components && AIQO.Components.Tabs) AIQO.Components.Tabs.init();
    if (AIQO.Components && AIQO.Components.GlobalSynthesis) AIQO.Components.GlobalSynthesis.init();

    // Navigation handlers (after components, so DOM hooks exist)
    if (window.reportNavigator && typeof window.reportNavigator.setupNavigationHandlers === 'function'){
      window.reportNavigator.setupNavigationHandlers();
    }

    if (AIQO.Components && AIQO.Components.QueryDetails) AIQO.Components.QueryDetails.init();
    initializeStatTooltips();
    initializeBootstrapTooltips();
  };
})();
